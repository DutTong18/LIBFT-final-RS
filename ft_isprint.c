#include <stdio.h>

int ft_isprint(char *str)
{
	while (*str != '\0')
	{
		if (*str >= 32 && *str <= 127)
		{
			str++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}

int main(void)
{
	printf("%d\n", ft_isprint("%^&*UHGHJKL:"));
}
